            <p align="justify">  
                Nesta etapa, iremos novamente implementar o nível G do MPS.BR, mas como uma pequena diferença, implementaremos utilizando metodologias 
                ágeis, ou, para simplificar, utilizaremos a metodologia scrum. A abordagem Scrum veio a agregar valor à definição de metodologias ágeis, 
                uma vez que esta propõe mudanças "radicais" a forma de se criar softwares, por assim dizer. Chegamos em tempos onde a interação passou a ser 
