<div>
    <div align="justify">
        <p>     
                A competitividade tomou conta do mundo dos negócios, em no mundo da tecnologia isso 
                não foi diferente. Mudanças tem ocorrido nos ambientes de negócio de forma a envolver
                as estruturas organizacionais e processos produtivos, uma vez que não possuimos mais
                aquela visão tradicional de alguns anos atrás, quando a tecnologia era menos evoluida
                nesta área. O estabelecimento de padrões para processos e formas de desenvolvimento (no 
                que se refere à todo o universo do desenvolvimento, principalmente a organização da documentação, 
                uma vez que este será o foco desta aplicação), tem se tornado um fator decisivo a levar
                empresas em uma linha crescente de evolução e a liderarem o mercado de software.
                A competitividade depende, cada vez mais, do estabelecimento de conexões nestas redes, 
                criando elos essenciais nas cadeias produtivas. Alcançar
                competitividade pela qualidade, para as empresas de software, implica tanto na
                melhoria da qualidade dos produtos de software e serviços correlatos, como dos
                processos de produção e distribuição de software.
        </p><br/>
        <p>
                Desta forma, assim como para outros setores, qualidade é fator crítico de sucesso
                para a indústria de software. Para que se tenha um setor de software competitivo,
                nacional e internacionalmente, é essencial que os empreendedores do setor
                coloquem a eficiência e a eficácia dos seus processos em foco nas empresas,
                visando à oferta de produtos de software e serviços correlatos conforme padrões
                internacionais de qualidade. O modelo MPS (Melhoria de Processo de Software) aparece com uma proposta adequada ao perfil de empresas com diferentes tamanhos 
                e características, públicas e privadas, com mais foco em empresas de micro, pequeno e médio
                porte. Este modelo propõe padrões de qualidade que possam ser aceitos internacionalmente, 
                juntamente com aproveitamentos de toda a competência existente em tais padrões e modelos de 
                melhoria de processo já disponíveis. O MPS propõe uma estrutura de sete níveis, graus de maturidade,
                que vão de G a A, em ordem crescente de dificuldade de implementação. Iremos propor padrões de 
                estrtura e de uso para os primeiros níveis, visando um acompanhamento adequando para empresas de 
                software que estão iniciando seus ramos no mundo dos negócios.
        </p><br/><br/>
    </div>
