                ser sempre adepto a mudanças, entre outras características que vieram com a abordagem da metodologia ágil. O Scrum apareceu então para inovar as 
                forma de pensar, de criar, voltado para a organização e gerenciamento de trabalhos complexos, não só, mas principalmente, projetos de 
                desenvolvimento de Software. <a href="http://www.desenvolvimentoagil.com.br/scrum/" target="_blank">Aqui</a> você poderá encontrar informações 
