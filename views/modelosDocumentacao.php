            <legend>Modelo básico</legend><br/>
            <p align = "justify">
                O nível G é denominado como o primeiro nível de maturidade de acordo com o Guia de Implementação 
                do MPS.BR (Melhoria de Processo de Software Brasileiro), com o objetivo de promover uma linha básica, um 
                norte para a organização, de forma que ela consiga gerenciar parcialmente seus projetos em desenvolvimento.
                Neste nível parcialmente gerenciado, possuimos dois processos vinculados a eles: 
               
                <u>
                  <li>   Gerência de Projetos</li><br/>
                  <li>   Gerência de Requisitos</li><br/><br/>
                </u>
                
                Tais processos possuem resultados esperados, assim como um propósito. O Guia Geral de Implentação do MPS.BR 
                define como propósito para a Gerência de Projetos a identificação, estabelecimento, coordenação e monitoramento 
                das atividades, tarefas e recursos necessários para um projeto produzir um produto e/ou serviço, no contexto dos requisitos 
                e restrições do projeto. O propósito da Gerência de Requisitos está relacionado com o gerenciamento dos requisitos dos 
                produtos e componentes do produto do projeto, identificando inconsistências entre os requisitos, os planos do projeto e os 
                produtos de trabalho do projeto.<br/><br/>
                
