            <legend>Modelo avançado</legend><br/>
            <p align="justify">
               O nível F é denominado como o segundo nível de maturidade de acordo com o Guia de Implementação do MPS.BR, onde 
               obtemos, dessa forma, um nível gerenciado do projetos de nossa empresa. As organizações que adotam o nível F começa a estruturar 
               seus projetos e suas principais fases, como planejamento e controle de versão. O gerente de projetos já passa a ter importância fundamental, 
               umas vez que ele toma para si a responsabilidade de atender aos objetivos nos prazos, custos e recursos previamente estipulados, além 
               de muitas outras variáveis. O nível G está contido no nível F, assim como o ambos estão contidos nos níveis mais abaixo deles, seguindo 
               uma ideia de conjuntos matemáticos. Assim sendo, para antendermos ao nível F, devemos atender primeiro ao nível G. Serão acrescentados 
               então, neste novo nível, cinco processos mais, sendo eles:
               <u>
                  <li>   Garantia da Qualidade</li><br/>
                  <li>   Medição</li><br/>
                  <li>   Aquisição</li><br/>
                  <li>   Gerência de Portifólio de Projetos</li><br/>
                  <li>   Gerência de Configuração</li><br/><br/>
                </u>
                Assim como no nível G, os processos possuem resultados esperados para eles e propósitos, elaborados pelo Guia Geral. A Garantia da Qualidade vem com o propósito 
                de assegurar que os produtos de trabalho e a execução dos processos estejam em conformidade com os planos e recursos predefinidos. O propósito 
                da medição é coletar, analisar e relatar dados relativos aos produtos desenvolvidos e aos processos implementados na organização 
                e em seus projetos, de forma a apoiar os objetivos da organização. A Aquisição define como objetivo o gerenciamento da aquisição 
                de produtos e serviços que satisfaçam as necessidades de quem as solicitou. O propósito da Gerencia de Portifólio é iniciar e manter
                projetos que sejam necessários, suficientes e sustentáveis, de forma a atender os objetivos estratégicos da organização.
                A Gerência de Configuração apresenta como objetivo o estabelecimento e manutenção da integridade de todos os produtos de trabalho 
                de um processo ou projeto e disponibilizá-los a todos os envolvidos.<br/><br/>
                
